    private void listarFunc(String nome) {
        ResultSet retornoFunc = func.consultarFuncionariosNome(nome);
        DefaultTableModel tabelaFunc = (DefaultTableModel) tblFuncionarios.getModel();
        tabelaFunc.setNumRows(0);

        try {
            do {
                tabelaFunc.addRow(new String[]{
                    retornoFunc.getString(1),
                    retornoFunc.getString(2),
                    retornoFunc.getString(3),
                    retornoFunc.getString(4),
                    retornoFunc.getString(5),
                    retornoFunc.getString(6)
                });
            } while(retornoFunc.next());
        } catch(SQLException ex) {
            Logger.getLogger(FHomeFunc.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Não existem funcionários cadastradas!");
        }
    }
    
