    private void tblFuncionariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFuncionariosMouseClicked
        int campo = tblFuncionarios.getSelectedRow();
        ResultSet retornoFunc = func.consultar(Integer.parseInt(tblFuncionarios.getValueAt(campo, 0).toString()));
        
        try {
            do {          
                txtId.setText(retornoFunc.getString(1));
                txtLogin.setText(retornoFunc.getString(6));
                txtSalario.setText(retornoFunc.getString(4));
                txtSituacao.setSelectedItem(retornoFunc.getString(5));
                txtNivel.setSelectedIndex(Integer.parseInt(retornoFunc.getString(7)));
            } while(retornoFunc.next());
        } catch(SQLException ex) {
            Logger.getLogger(FHomeFunc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tblFuncionariosMouseClicked

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if(!txtLogin.getText().equals("") && !txtSenha.getText().equals("")) {         
            if(!txtId.getText().equals("")) {
                func.atualizar(
                    Integer.parseInt(txtId.getText()),
                    Double.parseDouble(txtSalario.getText()),
                    txtSituacao.getSelectedItem().toString()
                );

                String hash = "";
                try {
                    hash = login.hashing(txtSenha.getText());
                } catch (NoSuchAlgorithmException | UnsupportedEncodingException ex) {
                    Logger.getLogger(FEditarFuncionario.class.getName()).log(Level.SEVERE, null, ex);
                }
                login.cadastrar(
                    Integer.parseInt(txtId.getText()),
                    txtLogin.getText(),
                    hash,
                    txtNivel.getSelectedIndex()
                );

                JOptionPane.showMessageDialog(null, "Dados cadastrados com sucesso!");
            } else {
                JOptionPane.showMessageDialog(null, "Selecione uma pessoa sem dados ou insira o ID Pessoa!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Preencha os campos login e senha!");
        }
        
        limparCampos();
        listarFunc("%");
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        if(!txtId.getText().equals("") && !txtLogin.getText().equals("")) {  
            func.atualizar(
                Integer.parseInt(txtId.getText()),
                Double.parseDouble(txtSalario.getText()),
                txtSituacao.getSelectedItem().toString()
            );
            
            String hash = "";
            if(!txtSenha.getText().equals("")) {
                try {
                    hash = login.hashing(txtSenha.getText());
                } catch (NoSuchAlgorithmException | UnsupportedEncodingException ex) {
                    Logger.getLogger(FEditarFuncionario.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                ResultSet retornoSenha = login.consultarSenha(txtLogin.getText());
                try {
                    hash = retornoSenha.getString(1);
                } catch (SQLException ex) {
                    Logger.getLogger(FEditarFuncionario.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            
            login.atualizar(
                Integer.parseInt(txtId.getText()),
                txtLogin.getText(),
                hash,
                txtNivel.getSelectedIndex()
            );
               
            JOptionPane.showMessageDialog(null, "Dados atualizados com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Preencha os campos necessários!");
        }
        
        limparCampos();
        listarFunc("%");
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(!txtId.getText().equals("") && !txtLogin.getText().equals("")) {
            login.excluir(Integer.parseInt(txtId.getText()));
            
            ResultSet retornoFunc = func.buscarIdPeloLogin(txtLogin.getText());
            try {
                ped.retirarAtendente(Integer.parseInt(retornoFunc.getString(1)));
            } catch (SQLException ex) {
                Logger.getLogger(FEditarPessoa.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            func.excluir(Integer.parseInt(txtId.getText()));
               
            JOptionPane.showMessageDialog(null, "Dados excluídos com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Preencha os campos necessários!");
        }
        
        limparCampos();
        listarFunc("%");
    }//GEN-LAST:event_btnExcluirActionPerformed

