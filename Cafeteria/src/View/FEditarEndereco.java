    private void tblEnderecosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEnderecosMouseClicked
        int campo = tblEnderecos.getSelectedRow();
        ResultSet retornoEnd = end.consultar(Integer.parseInt(tblEnderecos.getValueAt(campo, 0).toString()));
        
        try {
            do {          
                txtIdEnd.setText(retornoEnd.getString(1));
                txtIdFunc.setText(retornoEnd.getString(2));
                txtCep.setValue(retornoEnd.getString(3));
                txtEstado.setText(retornoEnd.getString(4));
                txtCidade.setText(retornoEnd.getString(5));
                txtBairro.setText(retornoEnd.getString(6));
                txtLogradouro.setText(retornoEnd.getString(7));
                txtNumero.setText(retornoEnd.getString(8));
                txtComplemento.setText(retornoEnd.getString(9));
            } while(retornoEnd.next());
        } catch(SQLException ex) {
            Logger.getLogger(FHomeFunc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tblEnderecosMouseClicked

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if(!txtIdFunc.getText().equals("")) {
            end.cadastrar(
                Integer.parseInt(txtIdFunc.getText()),
                txtCep.getText(),
                txtEstado.getText(),
                txtCidade.getText(),
                txtBairro.getText(),
                txtLogradouro.getText(),
                Integer.parseInt(txtNumero.getText()),
                txtComplemento.getText()
            );
               
            JOptionPane.showMessageDialog(null, "Dados cadastrados com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Preencha os campo ID Funcionário!");
        }
        
        limparCampos();
        listarEndereco("%");
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        if(!txtIdEnd.getText().equals("")) {
            end.atualizar(
                Integer.parseInt(txtIdEnd.getText()),
                Integer.parseInt(txtIdFunc.getText()),
                txtCep.getText(),
                txtEstado.getText(),
                txtCidade.getText(),
                txtBairro.getText(),
                txtLogradouro.getText(),
                Integer.parseInt(txtNumero.getText()),
                txtComplemento.getText()
            );
               
            JOptionPane.showMessageDialog(null, "Dados atualizados com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Preencha os campo ID Endereço!");
        }
        
        limparCampos();
        listarEndereco("%");
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(!txtIdEnd.getText().equals("")) {
            end.excluir(Integer.parseInt(txtIdEnd.getText()));
               
            JOptionPane.showMessageDialog(null, "Dados excluídos com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Preencha os campo ID Endereço!");
        }
        
        limparCampos();
        listarEndereco("%");
    }//GEN-LAST:event_btnExcluirActionPerformed

