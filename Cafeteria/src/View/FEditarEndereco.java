    private void listarEndereco(String nomeFunc) {
        ResultSet retornoEnd = end.listarEndereco(nomeFunc);
        DefaultTableModel tabelaEnd = (DefaultTableModel) tblEnderecos.getModel();
        tabelaEnd.setNumRows(0);

        try {
            do {
                tabelaEnd.addRow(new String[]{
                    retornoEnd.getString(1),
                    retornoEnd.getString(2),
                    retornoEnd.getString(3),
                    retornoEnd.getString(4),
                    retornoEnd.getString(5),
                    retornoEnd.getString(6)
                });
            } while(retornoEnd.next());
        } catch(SQLException ex) {
            Logger.getLogger(FHomeFunc.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Não existem endereços cadastrados!");
        }
    }
    
