    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        if(!txtProduto.getText().equals("")) {
            ResultSet retornoProdutos = prod.consultarNome(txtProduto.getText());
            DefaultTableModel tabela = (DefaultTableModel) tblProduto.getModel();
            tabela.setNumRows(0);

            try {
                do {
                    ResultSet retornoEstoque = est.consultar(Integer.parseInt(retornoProdutos.getString(1)));
                    tabela.addRow(new String[]{retornoProdutos.getString(1), retornoProdutos.getString(2), retornoEstoque.getString(2), retornoProdutos.getString(3)});
                } while(retornoProdutos.next());
            } catch(SQLException ex) {
                Logger.getLogger(FHomeFunc.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Não existem produtos com este nome!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Digite o nome do produto que deseja pesquisar!");
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

