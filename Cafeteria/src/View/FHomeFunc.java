    private void tblProdutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProdutoMouseClicked
        int campo = tblProduto.getSelectedRow();
        DefaultTableModel tabela = (DefaultTableModel) tblProduto.getModel();
        ResultSet retornoVendas = ped.vendasUnicas(Integer.parseInt(tabela.getValueAt(campo, 0).toString()));
        
        try {
            lblSaldoProduto.setText("R$ " + retornoVendas.getString(1));
            lblVendaProduto.setText(retornoVendas.getString(2));
        } catch (SQLException ex) {
            Logger.getLogger(FHomeFunc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tblProdutoMouseClicked

